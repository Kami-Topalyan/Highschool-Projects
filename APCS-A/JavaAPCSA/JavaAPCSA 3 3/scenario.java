public class scenario {
    public static void main(String[] args) {
        char c = 'A';
        String str1 = " Hello, ";
        String str2 = "World";

        System.out.println(c + str1 + "\t" + str2);
        System.out.println(c + str1 + "\b" + str2);
        System.out.println(c + str1 + "\n" + str2);
        System.out.println(c + str1 + "\r" + str2);
        System.out.println(c + str1 + "\f" + str2);
        System.out.println(c + str1 + "\'" + str2);
        System.out.println(c + str1 + "\"" + str2);
        System.out.println(c + str1 + "\\" +  str2);

    }
}